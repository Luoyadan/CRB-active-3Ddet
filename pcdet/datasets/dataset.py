    def vis_pc(self, data_dict):
        points = data_dict['points'][:, 0:3]
        # points = points[points[:,-1]==0]
        rgb = torch.tensor([30,144,255]).expand(points.shape[0],3).numpy()
        points = np.concatenate([points, rgb], axis=1)
        boxes = data_dict['gt_boxes']

        keep_classes = cfg.OPEN_WORLD.KNOWN_CLASS_NAMES


        all_indices = common_utils.keep_arrays_by_name(data_dict['gt_names'], self.class_names)
        known_indices = common_utils.keep_arrays_by_name(data_dict['gt_names'], keep_classes)

        unk_indices = []
        for i in all_indices:
            if i not in known_indices:
                unk_indices.append(i)
        if len(unk_indices) == 0:
            print("No UNK")
            return
        unk_indices = np.array(unk_indices)

        all_boxes = boxes[all_indices][:,:7]
        known_boxes = boxes[known_indices][:,:7]
        unk_boxes = boxes[unk_indices][:,:7]

        known_names = data_dict['gt_names'][known_indices]
        unk_names = data_dict['gt_names'][unk_indices]

        fg_point_masks = roiaware_pool3d_utils.points_in_boxes_cpu(points[:,:3], all_boxes)
        bg_points = points[fg_point_masks.sum(axis=0) == 0]

        known_point_masks = roiaware_pool3d_utils.points_in_boxes_cpu(points[:,:3], known_boxes)
        bg_unk_points = points[fg_point_masks.sum(axis=0) == 0]

        """------- Draw BG points -------"""
        rgb = np.zeros((points.shape[0], 3))
        rgb[:, ] = [30,144,255]  # bg points color is white
        points_rgb = np.array(
            [[p[0], p[1], p[2], c[0], c[1], c[2]] for p, c in
             zip(points, rgb)])

        boxes_to_draw = []
        """------- Draw known boxes -------"""
        if known_boxes is not None:
            for i in range(len(boxes_to_corners_3d(known_boxes).tolist())):
                box = boxes_to_corners_3d(known_boxes).tolist()[i]
                label = "{}".format(known_names[i])

                boxes_ref_label = {
                    "corners": list(box),
                    # optionally customize each label
                    "label": None,
                    "color": [0, 255, 0], # green 0, 255, 0
                }
                boxes_to_draw.append(boxes_ref_label)


        """------- Draw UNK boxes -------"""
        if unk_boxes is not None:
            for i in range(len(boxes_to_corners_3d(unk_boxes).tolist())):
                box = boxes_to_corners_3d(unk_boxes).tolist()[i]
                label = "{}".format(unk_names[i])

                boxes_ref_label = {
                    "corners": list(box),
                    # optionally customize each label
                    "label": None,
                    "color": [0, 255, 0], # red[255, 0, 0]  yellow: 255, 255, 0
                }
                boxes_to_draw.append(boxes_ref_label)

        boxes_to_draw = np.array(boxes_to_draw)

        wandb.log(
            {"3d point cloud":
                wandb.Object3D({
                    "type": "lidar/beta",
                    "points": points_rgb,
                    "boxes": boxes_to_draw
                })
            })

